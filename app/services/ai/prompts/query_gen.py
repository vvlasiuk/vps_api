def build_user_prompt(describe: dict, task: str, sys_fields: list = None, alias: str = "т",
                      current_sel: str = "", current_meta: dict = None) -> str:
    """Складає user-промпт: опис об'єкта + системні поля + (опційно) поточний запит + завдання."""
    object_type = describe.get("type", "")
    object_name = describe.get("name", "")
    synonym = describe.get("synonym", "")

    # Стислий перелік реквізитів для контексту (ім'я, синонім, типи)
    attrs_lines = []
    for a in describe.get("attributes", []) or []:
        types_desc = _types_desc(a.get("types", []))
        syn = a.get("synonym", "") or ""
        attrs_lines.append(f"- {a.get('name','')} ({syn}): {types_desc}")

    # Табличні частини — коротко (імена + їх реквізити)
    ts_lines = []
    for ts in describe.get("tabular_sections", []) or []:
        ts_attrs = ", ".join(x.get("name", "") for x in (ts.get("attributes", []) or []))
        ts_lines.append(f"- {ts.get('name','')} ({ts.get('synonym','')}): {ts_attrs}")

    # Режим редагування визначаємо раніше — від нього залежить подача реквізитів
    has_current = bool((current_sel or "").strip()) or bool(current_meta)

    # Табличні частини — коротко (імена + їх реквізити)
    ts_lines = []
    for ts in describe.get("tabular_sections", []) or []:
        ts_attrs = ", ".join(x.get("name", "") for x in (ts.get("attributes", []) or []))
        ts_lines.append(f"- {ts.get('name','')} ({ts.get('synonym','')}): {ts_attrs}")

    # Заголовок переліку реквізитів: у режимі редагування — лише як ДОВІДКА, не запрошення додавати
    attrs_header = (
        "РЕКВІЗИТИ ОБ'ЄКТА (довідка — використовуй ЛИШЕ якщо завдання прямо просить додати поле; НЕ додавай сам):"
        if has_current else
        "РЕКВІЗИТИ:"
    )

    parts = [
        f"ОБ'ЄКТ: {object_type}.{object_name}" + (f" — {synonym}" if synonym else ""),
        f"ПСЕВДОНІМ ТАБЛИЦІ у .sel: {alias}",
        "",
        attrs_header,
        "\n".join(attrs_lines) if attrs_lines else "(немає)",
    ]
    if ts_lines:
        parts += ["", "ТАБЛИЧНІ ЧАСТИНИ:", "\n".join(ts_lines)]

    # Системні поля — обов'язкові, з готовими виразами
    if sys_fields:
        sf_lines = []
        for sf in sys_fields:
            sf_lines.append(
                f"- {sf['expr']} КАК {sf['key']}  (type={sf['type']}, info={sf['info']})"
            )
        parts += [
            "",
            "СИСТЕМНІ ПОЛЯ (обов'язково включити згори .sel і у fields, точно як тут):",
            "\n".join(sf_lines),
        ]

    # Поточний запит (режим редагування)
    if has_current:
        import json as _json
        meta_text = ""
        try:
            meta_text = _json.dumps(current_meta, ensure_ascii=False, indent=2) if current_meta else ""
        except (TypeError, ValueError):
            meta_text = ""
        parts += [
            "",
            "ПОТОЧНИЙ ЗАПИТ (редагуй його за завданням, зберігаючи решту БАЙТ-У-БАЙТ):",
            ".sel:",
            (current_sel or "").strip() or "(порожній)",
            ".json:",
            meta_text or "(порожній)",
        ]

    parts += [
        "",
        "ЗАВДАННЯ КОРИСТУВАЧА:",
        task.strip(),
        "",
        ("Онови {sel, meta} за завданням, взявши поточний запит за основу."
         if has_current else
         "Згенеруй {sel, meta} за правилами із системної інструкції."),
    ]
    return "\n".join(parts)


def _types_desc(types: list) -> str:
    """Людський опис типів реквізиту для контексту промпта."""
    if not types:
        return "?"
    out = []
    for t in types:
        if t.get("kind") == "ref":
            obj = t.get("object", "")
            out.append("посилання→" + (obj.split(".", 1)[1] if "." in obj else obj))
        else:
            out.append(t.get("name", ""))
    return ", ".join(out)